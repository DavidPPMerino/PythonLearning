# Comando Ansi
'''
\033[m = Limpar formatação
\033[0;0;0m = estilo padrao [estilo;texto;fundom

'''
# Estilos
'''
0 = None
1 = Bold
4 = Underline
7 = Negative
'''
# Cores de texto
'''
30 = branco
31 = vermelho
32 = verde
33 = amarelo
34 = azul
35 = roxo
36 = ciano
37 = cinza
'''
# Cores de fundo
'''
40 = branco
41 = vermelho
42 = verde
43 = amarelo
44 = azul
45 = roxo
46 = ciano
47 = cinza
'''

# utilizar preto na letra
'''
\033[7;30m
'''

